// Range Operators and Loops
// Department Store Program
// Send Discount vouchers to customers between the ages of 18 and 30

// firstNumber...lastNumber
// closed range operator
let myRange = 10...20

for Range in 10...20 {
    print(Range)
}

print("")

// half open range operator
let myRange2 = 10..<20 

for Range2 in 10..<20 {
    print(Range2)
}